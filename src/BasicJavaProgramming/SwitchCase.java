package BasicJavaProgramming;

public class SwitchCase {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        switch (a) {
            case 1:
                System.out.println(a);
                break;
                case 2:
                    System.out.println(b);
                    break;
                    case 3:
                        System.out.println(a);
                        break;
            case 4:
                System.out.println(b);
                    break;
                }
            }
        }




