package BasicJavaProgramming;

public class WhileConcept {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        int a = 1, k = 2;
        for (a = 1; a <= 10; a++) {
            System.out.println(k + "*" + a + "=" + (a * k));
        }
        int r = 1;
        do {
            System.out.println(r);
            r++;
        } while (r <= 5) ;
        }

    }








